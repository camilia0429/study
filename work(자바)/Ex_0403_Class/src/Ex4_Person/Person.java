package Ex4_Person;

public class Person {
	private String name;
	private int age;
	private String tel;
	
//	setter & getter
	public void setAge(int a) {
		age = a;
	}
	public int getAge() {
		return age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
	
	
	

}
